#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	ch;

	ch = (unsigned char)c;
	while (*s != ch)
	{
		if (!*s)
			return (NULL);
		s++;
	}
	return ((char *)s);
}
